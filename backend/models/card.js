const mongoose = require('mongoose');
const { Schema } = mongoose;

const cardSchema = new Schema({
  name: {
    type: String,
    required: true,
    minlength: 2,
    maxlength: 30,
  },
  link: {
    type: String,
    required: true,
    match: [
      /http[s]?:\/\/(?:[\w]{3}\.)?[\w\d-]+\.[\w]+(?:\/[\w.-]+)*\/?/,
      'El enlace de la imagen no es v√°lido',
    ],
  },
  owner: {
    type: mongoose.Schema.Types.ObjectId,
    ref: 'user',
    required: true,
  },
  likes: {
    type: [mongoose.Schema.Types.ObjectId],
    ref: 'user',
    default: [],
  },
  createdAt: {
    type: Date,
    default: Date.now,
  },
});

module.exports = mongoose.model('card', cardSchema);
